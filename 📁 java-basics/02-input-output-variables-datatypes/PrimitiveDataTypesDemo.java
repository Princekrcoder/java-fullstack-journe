public  class PrimitiveDataTypesDemo {
    public static void main(String[] args) {
        // Primitive Data Types in Java
        int intValue = 100;               // Integer (whole number)
        double doubleValue = 99.99;       // Floating point number
        char charValue = 'A';             // Single character
        boolean booleanValue = true;      // true or false
        String stringValue = "Hello";    // Sequence of characters (not primitive but commonly used)

        // Print the values
        System.out.println("Integer Value: " + intValue);
        System.out.println("Double Value: " + doubleValue);
        System.out.println("Character Value: " + charValue);
        System.out.println("Boolean Value: " + booleanValue);
    }
}